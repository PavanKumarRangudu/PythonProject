def caesar(direction, text, shift):
    cipher_text = ""
    if direction == "decode":
        shift *= -1
    for char in text:
        if char in alphabet:
            position = alphabet.index(char)
            new_position = position + shift
            cipher_text += alphabet[new_position]
        else:
            cipher_text += char
            # print("Error: Invalid input. Please enter a valid message.")

    print(f"Here is the {direction}d message: {cipher_text}")

choice = "yes"
